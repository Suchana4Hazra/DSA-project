package board;

public class triple {

    public int row;
    public int col;
    public int num;

    public triple() {

        this.row = row;
        this.col = col;
        this.num = num;
    }

    public triple(int r, int c, int n) {

        this.row = r;
        this.col = c;
        this.num = n;
    }
}
